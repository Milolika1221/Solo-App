    async def send_supplement_reminder(self, supplement_time: str):
        """
        Отправляет напоминание о витаминах и добавках.
        
        Args:
            supplement_time: 'morning', 'preworkout' или 'evening'
        """
        try:
            cursor = self.db.execute("SELECT user_id FROM users")
            users = cursor.fetchall()
            
            routine = self.get_supplement_routine(supplement_time)
            callback_data = f"supplement_{supplement_time}"
            
            time_names = {
                "morning": "утра",
                "preworkout": "дня",
                "evening": "вечера"
            }
            time_name = time_names.get(supplement_time, "")
            
            for (user_id,) in users:
                try:
                    keyboard = InlineKeyboardMarkup(
                        inline_keyboard=[
                            [InlineKeyboardButton(
                                text=f"{Emoji.SUCCESS} Принял витамины",
                                callback_data=callback_data
                            )]
                        ]
                    )
                    
                    await self.bot.send_message(
                        user_id,
                        f"💊 <b>Время витаминов, {time_name}!</b> {Emoji.SPARKLES}\n\n"
                        f"{routine}\n\n"
                        f"{Emoji.GIFT} +3 EXP за прием витаминов",
                        reply_markup=keyboard
                    )
                except Exception as e:
                    logger.error(f"{Emoji.ERROR} Не удалось отправить напоминание о витаминах пользователю {user_id}: {e}")
            
            logger.info(f"{Emoji.SUCCESS} Напоминания о витаминах ({supplement_time}) отправлены {len(users)} пользователям")
            
        except Exception as e:
            logger.error(f"{Emoji.ERROR} Ошибка отправки напоминаний о витаминах: {e}")
    
